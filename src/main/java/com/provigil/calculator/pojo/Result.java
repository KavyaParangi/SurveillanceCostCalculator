package com.provigil.calculator.pojo;

public class Result {
	
	int id;
	int cost;
	public Result() {
		super();
	}
	public Result(int id, int cost) {
		super();
		this.id = id;
		this.cost = cost;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	
	
	
	
}
